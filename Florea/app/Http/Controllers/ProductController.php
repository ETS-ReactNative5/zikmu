    /**
     * Display the specified resource.
     *
     * @param  \App\Models\Product  $product
     * @return \Illuminate\Http\Response
     */
    public function show(Product $product)
    {
        $category = Category::where('id', $product->category_id)->get()[0];
        $sameCatProducts = Product::where('category_id', $product->category_id)->where('id', '!=', $product->id)->where('visible', '!=', 0)->get();
        return view('pages.products.show', ['product' => $product, 'category' => $category, 'sameCatProducts' => $sameCatProducts]);
    }

